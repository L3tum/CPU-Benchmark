<h2><span class='editsection'>[<a href='/w/index.php?title=Hello,_world!&amp;action=edit&amp;section=1' title='Редактировать раздел «Перевод на русский язык»'>править</a>]</span> <span class='mw-headline' id='.D0.9F.D0.B5.D1.80.D0.B5.D0.B2.D0.BE.D0.B4_.D0.BD.D0.B0_.D1.80.D1.83.D1.81.D1.81.D0.BA.D0.B8.D0.B9_.D1.8F.D0.B7.D1.8B.D0.BA'>Перевод на русский язык</span></h2>
<p>Русскоязычные программисты и технические переводчики традиционно переводят слово world в его основном значении&#160;— «мир, свет, вселенная», отчего из «hello world» получается дословное «здравствуй, мир». Объясняется такой перевод тем, что программа, начиная работу, как бы рождается и приветствует мир, в который она приходит.</p>
<p>Более знакомые с тонкостями английского языка указывают на то, что у world имеются и другие значения&#160;— «народ», «общество», «человечество»,<sup id='cite_ref-1' class='reference'><a href='#cite_note-1'>[1]</a></sup> а «hello world» является распространенным неформальным приветствием, адресованным неопределённому кругу лиц (людей, а не просто произвольных объектов или природе в целом). Поэтому переводить приветствие следует как «всем привет», «привет, народ», «здорово, люди» и&#160;т.&#160;п., что подтверждается англоязычными программистами.<sup id='cite_ref-2' class='reference'><a href='#cite_note-2'>[2]</a></sup><sup id='cite_ref-3' class='reference'><a href='#cite_note-3'>[3]</a></sup></p>
<p>В аналогичном смысле слово world применяется при назначении <a href='/wiki/%D0%9F%D1%80%D0%B0%D0%B2%D0%B0_%D0%B4%D0%BE%D1%81%D1%82%D1%83%D0%BF%D0%B0' title='Права доступа'>прав доступа</a> (owner, group, world) в операционных системах <a href='/wiki/UNIX' title='UNIX'>UNIX</a>, для которых был разработан язык <a href='/wiki/%D0%A1%D0%B8_(%D1%8F%D0%B7%D1%8B%D0%BA_%D0%BF%D1%80%D0%BE%D0%B3%D1%80%D0%B0%D0%BC%D0%BC%D0%B8%D1%80%D0%BE%D0%B2%D0%B0%D0%BD%D0%B8%D1%8F)' title='Си (язык программирования)'>C</a>, откуда и пошла традиция выводить «hello world» в качестве приветствия. <i>owner</i> означает владельца объекта, <i>group</i>&#160;— группу пользователей, в которую входит владелец, а <i>world</i>&#160;— всех остальных пользователей системы.</p>
<h2><span class='editsection'>[<a href='/w/index.php?title=Hello,_world!&amp;action=edit&amp;section=2' title='Редактировать раздел «Интересные факты»'>править</a>]</span> <span class='mw-headline' id='.D0.98.D0.BD.D1.82.D0.B5.D1.80.D0.B5.D1.81.D0.BD.D1.8B.D0.B5_.D1.84.D0.B0.D0.BA.D1.82.D1.8B'>Интересные факты</span></h2>
